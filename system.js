function aisatuShuffle(array) 
{
    for(let i = (array.length - 1); 0 < i; i--)
    {  
      // 0〜(i+1)の範囲で値を取得
      let r = Math.floor(Math.random() * (i + 1));
  
      // 要素の並び替え
      let tmp = array[i];
      array[i] = array[r];
      array[r] = tmp;
    }
    return array;
}